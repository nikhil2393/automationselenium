package test;

public class ArithmeticOperators {

	public static void main(String[] args) {
		// Addition
		int a=10;
		int b=20;
		
		int sum= a+b;
		
		System.out.println(sum);
		
		//substraction
		int sub= b-a;
		System.out.println(sub);
		
		//Division
		
		int div= b/a;
		System.out.println(div);
		
		//Multiplication
		
		int mul=a*b;
		System.out.println(mul);
		
		//percentage
		
		int mod=a%b;
		System.out.println(mod);
	
	}
		

		
}
